async function classifyMerchantsBatch(
	batch: MerchantProfile[]
): Promise<MerchantClassification[]> {
	const systemPrompt = `Du er en Norwegian bank transaction classifier. 
Din jobb er å klassifisere handlende/mottakere fra norske banktransaksjoner i kategorier og underkategorier.

Tilgjengelige kategorier (bruk kun disse):
- dagligvare (mat fra butikk)
- mat (restaurant, takeaway, kafe)
- bolig (husleie, strøm, felleskostnader, internett hjemme)
- lån (huslån, billån, forbrukslån, studielån, rammelån, terminbeløp)
- transport (kollektivtransport, drivstoff, parkering, bompenger, elbillading, fly, taxi)
- helse (lege, tannlege, apotek, fysioterapi)
- abonnement (streaming, mobilabonnement, software, magasin)
- underholdning (kino, konserter, gym, sport)
- shopping (klær, elektronikk, møbler, byggevarer, håndverkere)
- barn (barnehage, aks, sfo, svømmekurs, idrettslag, kulturskole)
- forsikring (bilforsikring, innboforsikring, reiseforsikring, sykkelforsikring, livsforsikring)
- sparing (sparekontoer, fond, aksjer)
- overføring (Vipps, overføringer til konti)
- lønn (inntekt, lønn, NAV-ytelser)
- annet (alt som ikke passer)

Regler:
- isFixed = true hvis betalingen er periodisk/fast (samme beløp, samme mottaker, månedlig/kvartal)
- Lav cv (< 0.15) + høy monthsActive = typisk fast utgift
- Lag et kort, menneskelesbart label på norsk
- subcategory er valgfri men bidrar til bedre innsikt (f.eks. "huslån", "barnehage", "bilforsikring")`.trim();

	const fewShotInput = JSON.stringify(FEW_SHOT_EXAMPLES, null, 0);
	const fewShotOutput = JSON.stringify({ classifications: FEW_SHOT_OUTPUTS }, null, 0);

	const userPrompt = `Klassifiser disse handlende/mottakerne:
${JSON.stringify(
	batch.map((p) => ({
		merchantKey: p.merchantKey,
		exampleDescription: p.exampleDescription,
		txCount: p.txCount,
		avgAmount: p.avgAmount,
		monthsActive: p.monthsActive,
		cv: p.cv
	})),
	null,
	0
)}

Returner JSON med ett objekt per handlende i samma rekkefølge:
{ "classifications": [{ "merchantKey", "category", "subcategory", "label", "emoji", "isFixed" }, ...] }`;

	const response = await openai.chat.completions.create({
		model: 'gpt-4o-mini',
		response_format: { type: 'json_object' },
		messages: [
			{ role: 'system', content: systemPrompt },
			{ role: 'user', content: fewShotInput },
			{ role: 'assistant', content: fewShotOutput },
			{ role: 'user', content: userPrompt }
		],
		temperature: 0.1,
		max_tokens: 4000
	});

	const raw = response.choices[0]?.message?.content ?? '{}';
	let parsed: { classifications?: MerchantClassification[] };
	try {
		parsed = JSON.parse(raw);
	} catch {
		console.error('[spending-analyzer] Failed to parse LLM response:', raw);
		return [];
	}

	return (parsed.classifications ?? []).filter(
		(c) => c.merchantKey && c.category && c.label
	);
}

// ─── LLM: generate human-readable insights ───────────────────────────────────

async function generateInsights(
	profiles: Array<{
		merchantKey: string;
		exampleDescription: string;
		totalAmount: number;
		avgMonthlyAmount: number;
		txCount: number;
		monthsActive: number;
		cv: number;
		firstMonth: string;
		lastMonth: string;
		mappedCategory: string;
		mappedLabel: string;
		mappedIsFixed: boolean;
	}>,
	totalTxCount: number
): Promise<string[]> {
	// Build a compact summary for the LLM
	const fixedTotal = profiles.filter((p) => p.mappedIsFixed).reduce((s, p) => s + p.totalAmount, 0);
	const variableTotal = profiles.filter((p) => !p.mappedIsFixed).reduce((s, p) => s + p.totalAmount, 0);

	// Top 20 by total amount for insight context
	const top20 = profiles.slice(0, 20).map((p) => ({
		label: p.mappedLabel,
		category: p.mappedCategory,
		totalAmount: p.totalAmount,
		avgMonthly: p.avgMonthlyAmount,
		txCount: p.txCount,
		monthsActive: p.monthsActive,
		isFixed: p.mappedIsFixed
	}));

	const prompt = `Analyser disse norske banktransaksjonsdataene og generer 4-6 korte, konkrete innsikter på norsk.

Totalt ${totalTxCount} transaksjoner. Faste utgifter: ${Math.round(fixedTotal / 1000)}k NOK. Variable: ${Math.round(variableTotal / 1000)}k NOK.

Topp mottakere (siste 13 mnd):
${JSON.stringify(top20, null, 0)}

Regler for innsikter:
- Nevn konkrete tall i NOK
- Identifiser mønstre som prosjekter, sesongvariasjoner, uvanlige utgifter
- Vurder "faste vs variable"-balansen
- Legg merke til om noen kategorier dominerer uventet
- Hvert punkt skal starte med en emoji
- Maks 1 setning per punkt

Returner JSON: { "insights": ["...", "...", ...] }`;

	try {
		const response = await openai.chat.completions.create({
			model: 'gpt-4o-mini',
			response_format: { type: 'json_object' },
			messages: [{ role: 'user', content: prompt }],
			temperature: 0.4,
			max_tokens: 600
		});

		const raw = response.choices[0]?.message?.content ?? '{}';
		const parsed = JSON.parse(raw);
		return parsed.insights ?? [];
	} catch (e) {
		console.error('[spending-analyzer] Insights generation failed:', e);
		return [];
	}
}
